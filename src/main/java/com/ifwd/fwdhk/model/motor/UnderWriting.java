package com.ifwd.fwdhk.model.motor;

public class UnderWriting {
	
	private String underWriteNo;
	private String underWriteDesc;
	private boolean underWriteAns;
	
	public String getUnderWriteNo() {
		return underWriteNo;
	}
	public void setUnderWriteNo(String underWriteNo) {
		this.underWriteNo = underWriteNo;
	}
	public String getUnderWriteDesc() {
		return underWriteDesc;
	}
	public void setUnderWriteDesc(String underWriteDesc) {
		this.underWriteDesc = underWriteDesc;
	}
	public boolean isUnderWriteAns() {
		return underWriteAns;
	}
	public void setUnderWriteAns(boolean underWriteAns) {
		this.underWriteAns = underWriteAns;
	}	

}
