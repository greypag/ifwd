package com.ifwd.fwdhk.model;



public class ProvieRiderEligibility {
	
	private boolean accidentalDeathBenefit;
	private boolean cancerBenefit;
	private boolean termLifeBenefit;
	
	public boolean isAccidentalDeathBenefit() {
		return accidentalDeathBenefit;
	}
	public void setAccidentalDeathBenefit(boolean accidentalDeathBenefit) {
		this.accidentalDeathBenefit = accidentalDeathBenefit;
	}
	public boolean isCancerBenefit() {
		return cancerBenefit;
	}
	public void setCancerBenefit(boolean cancerBenefit) {
		this.cancerBenefit = cancerBenefit;
	}
	public boolean isTermLifeBenefit() {
		return termLifeBenefit;
	}
	public void setTermLifeBenefit(boolean termLifeBenefit) {
		this.termLifeBenefit = termLifeBenefit;
	}
	

}
