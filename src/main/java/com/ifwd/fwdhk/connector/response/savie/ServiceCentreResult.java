package com.ifwd.fwdhk.connector.response.savie;

public class ServiceCentreResult{
	private String id;
	private String serviceCentreName;
	private String address;
	private String operationHours;
	private String map;
	private String phone;
	private String fax;
	private String serviceCentreCode;
	private String lang;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getServiceCentreName() {
		return serviceCentreName;
	}
	public void setServiceCentreName(String serviceCentreName) {
		this.serviceCentreName = serviceCentreName;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getOperationHours() {
		return operationHours;
	}
	public void setOperationHours(String operationHours) {
		this.operationHours = operationHours;
	}
	public String getMap() {
		return map;
	}
	public void setMap(String map) {
		this.map = map;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getFax() {
		return fax;
	}
	public void setFax(String fax) {
		this.fax = fax;
	}
	public String getServiceCentreCode() {
		return serviceCentreCode;
	}
	public void setServiceCentreCode(String serviceCentreCode) {
		this.serviceCentreCode = serviceCentreCode;
	}
	public String getLang() {
		return lang;
	}
	public void setLang(String lang) {
		this.lang = lang;
	}
	
	
}
